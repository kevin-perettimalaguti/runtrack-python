    if nombre < 0:
        print (f"Le nombre {nombre} est de nature négatif")
    
    elif nombre > 0:
        print (f"Le nombre {nombre} est de nature postive")

    else:
        print (f"Tu ne peux pas choisir ceci car c'est un chiffre neutre ou un autre cactère(s)")

